		<h2 id="1">Чем занимается наша компания?</h2>
			Для связи с другим пользователем по Интернету необходимо ввести соответствующий адрес в компьютере: имя или номер.<br>
			Данный адрес должен быть уникальным, чтобы компьютеры могли найти друг друга. <br>
			Мы осуществляем координацию этих уникальных идентификаторов по всему миру. <br>
			Без этого не существовало бы глобального Интернета.<br>
		<br>
		<br>
		<h2 id="2">Что такое система доменных имен?</h2>
			Система доменных имен (DNS) создана для того, чтобы сделать Интернет доступным для людей. <br>
			Главный способ, которым компьютеры, составляющие Интернет, <br>
			находят друг друга - это серии номеров, где каждый номер (так называемый IP-адрес) относится к отдельному компьютеру. <br>
			Однако людям было бы сложно запоминать длинные списки номеров, <br>
			поэтому в DNS чаще используются буквы, а не цифры, <br>
			и определенные наборы букв связываются с определенными наборами цифр.<br>
			<br>
			В итоге сайт можно найти по адресу, например “google.com”, а не только по адресу, состоящему из цифр – <br>
			по которому его находят в сети компьютеры. Одно из преимуществ этой системы - помимо того, <br>
			что сеть становится более простой в использовании для людей - то, <br>
			что конкретный домен не должен быть привязан к конкретному компьютеру, <br>
			потому что связь между доменом и его IP-адресом можно изменить легко и просто. <br>
			В течение 48 часов такое изменение станет известно всему Интернету, <br>
			благодаря постоянно обновляющейся инфраструктуре DNS. <br>
			В результате система оказывается достаточно гибкой.<br>
			<br>
			В доменном имени можно выделить два элемента: до точки и после нее. Часть, которая находится справа от точки, <br>
			например “com”, “net”, “org” и т.д., называется "доменом верхнего уровня" или TLD. <br>
			В каждом случае одна компания (называемая реестром) отвечает за все домены, <br>
			заканчивающиеся определенным TLD, и имеет доступ к полному списку доменов под этим именем, <br>
			как и к списку IP-адресов, с которыми связаны эти имена. Часть перед точкой - это доменное имя, <br>
			которое вы регистрируете и которое затем используется в интернет-системах, например на веб-сайтах, в электронной почте и т.д. <br>
			Такие домены продаются большим количеством регистраторов или предоставляются бесплатно на их усмотрение, <br>
			хотя в каждом случае они платят налог с домена определенному реестру, под именем которого домен был зарегистрирован.<br>
		<br>
