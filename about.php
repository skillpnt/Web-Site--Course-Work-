		<h2 id="3">Что мы делаем с IP-адресами?</h2>
			Наша компания играет административную роль по отношению к IP-адресам, которые используют компьютеры, так же, <br>
			как и по отношению к доменным именам, которые используют люди. <br>
			По той же причине, по которой не может быть двух одинаковых доменных имен, <br>
			не может быть и двух одинаковых IP-адресов.<br>
			<br>
			Опять-таки, мы не управляет системой, а помогает координировать поддержку IP-адресов, <br>
			чтобы избежать повторений или пересечений. Наша компания также является центральным репозиторием для IP-адресов, <br>
			в котором определяются диапазоны региональных реестров, которые в свою очередь распространяют их на провайдеров сети.<br>
		<br>
		<br>
		<h2 id="4">Что насчет корневых серверов?</h2>
			Корневые серверы - это тоже отдельный вопрос. Существует 13 корневых серверов – или, точнее, <br>
			13 IP-адресов в Интернете, по которым можно найти корневые серверы <br>
			(серверы, которые имеют один из этих 13 IP-адресов, физически могут находиться в десятках разных мест). <br>
			На всех этих серверах хранится копия одного и того же файла, который действует как главный список адресных книг Интернета. <br>
			В нем указаны адреса всех доменов верхнего уровня (.com, .de и т.д.),<br> 
			по которым можно найти адресную книгу определенного реестра.<br>
			<br>
			На самом деле, к корневым серверам обращаются не слишком часто (с учетом размера Интернета), <br>
			поскольку однажды узнав адрес определенного домена верхнего уровня, компьютеры сети запоминают его, <br>
			проверяя только изредка, чтобы удостовериться, что он не изменился. <br>
			Тем не менее, корневые серверы остаются активными для того, чтобы Интернет мог функционировать.<br>
